package guia8ejex2.entidades;

/**
 *
 * @author Juan
 */
public class NIFs {
   private long DNI;
   private char letra;

    public NIFs() {
    }
    public NIFs(long DNI, char letra) {
        this.DNI = DNI;
        this.letra = letra;
    }

    public long getDNI() {
        return DNI;
    }

    public void setDNI(long DNI) {
        this.DNI = DNI;
    }

    public char getLetra() {
        return letra;
    }

    public void setLetra(char letra) {
        this.letra = letra;
    }
   
}
